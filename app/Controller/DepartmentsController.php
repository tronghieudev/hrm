App::uses('ADController', 'Controller');
App::uses('Department', 'Model');
/**
 * class Department
 * @property ADController
 * @property Department
 * manage positon staff in company
*/
class DepartmentsController extends  ADController {
    public $components = array('Paginator');

    public $paginate = array(
        'limit' => 20,
        'conditions' => array(
            'Department.status' => 1
        ),
    );

    public function beforeFilter() {
        parent::beforeFilter(); // TODO: Change the autogenerated stub
        $this->loadModel('Department');
        $this->layout = 'admin';
    }

    /**
     * function index get list Department
    */
    public function admin_index() {

//        $input = $this->request->query;
        $this->Paginator->settings = $this->paginate;
        $data = $this->Paginator->paginate('Department');
        $this->set('data', $data);
    }


    /**
     * function post create - method ajax
     * @return json
     */
    public function admin_create(){

        $this->autoRender = false;
        if($this->request->is('ajax')) {

            // validate
            $this->Department->set($this->request->data);
            if(!$this->Department->validates()) {
                return $this->_falseJson(Constants::BAD_REQUEST, null, $this->Department->validationErrors);
            }

            $this->Department->create();
            $model = $this->Department->save($this->request->data);
            if($model) {
                return $this->_trueJson(['message' => 'Create new Department success!']);
            }
            return $this->_falseJson(Constants::BAD_REQUEST, null, ['message' => 'Create new Department false!']);
        }
    }

    /**
     * function get by id - method ajax
     * @return json
    */
    public function admin_getById() {

        $this->autoRender = false;
        if($this->request->is('ajax')) {
            $id = $this->request->query['id'];
            $data = $this->Department->findById($id);
            if($data) {
                return $this->_trueJson($data['Department']);
            }
            return $this->_falseJson(Constants::BAD_REQUEST, null, ['message' => 'Data is null']);
        }
    }

    /**
     * function edit Department by method ajax
    */
    public function admin_edit(){

        $this->autoRender = false;
        if($this->request->is('ajax')) {

            // validate
            $this->Department->set($this->request->data);
            if(!$this->Department->validates()) {
                return $this->_falseJson(Constants::BAD_REQUEST, null, $this->Department->validationErrors);
            }

            $this->Department->id = $this->request->data['id'];
            $model = $this->Department->save($this->request->data);
            if($model) {
                return $this->_trueJson(['message' => 'Edit success!']);
            }
            return $this->_falseJson(Constants::BAD_REQUEST, null, ['message' => 'Edit false!']);
        }
    }

    /**
     * function ajax remove postion
     * @
     * @return json
    */
    public function admin_remove() {

        $this->autoRender = false;
        if($this->request->is('ajax')) {
            $this->Department->validate = [];
            $this->Department->id = $this->request->query['id'];
            $model = $this->Department->save(['status' => 0]);
            if($model) {
                return $this->_trueJson(['message' => 'Remove item success!']);
            }
            return $this->_falseJson(Constants::BAD_REQUEST, null, ['message' => 'Remove item false!']);
        }
    }
}